def vogal(x):
    if x=="a" or x=="A":
        return True
    else:
        if x=="e" or x=="E":
            return True
        else:
            if x=="i" or x=="I":
                return True
            else:
                if x=="o" or x=="O":
                    return True
                else:
                    if x=="u" or x=="U":
                        return True
                    else:
                        return False
